package Operators;

public class Arithematic {
    public static void main(String[] args){
        // Arithematic operations in Java are performed using operators.
        //The basic arithematic opertors are +, -, *, /, and %.

        int a = 10;
        int b = 5;

        int sum = a + b; // Addition
        int difference = a - b; // Subtarction
        int product = a * b; //Multiplication
        int quotient = a / b; // Division
        int remainder = a % b; // Modulus

        // To print the results, we use System.out.println()
        System.out.println("Sum:" + sum); // this will print the sum of a and b
        System.out.println("Difference:" + difference); // this will print the difference between a and b
        System.out.println("Product:" + product); // this will print the product of a and b
        System.out.println("Quotient:" + quotient); // this will print the quotient of a and b
        System.out.println("Remainder:" + remainder); // this will print the remainder of a divided by b
        
        
    }
    
}
