
public class variables_part2 {
    public static void main(String[] args){
        // so here we are going to deal with strings
        // A char is a single character e.g 'a'
        // A string is a combination of characters e.g "Hello World"

        char firstLetter = 'H'; // char is used for single characters
        String greeting = "Hello World"; // String is used for a sequence of characters

        // To print the above values, we use System.out.println()
        System.out.println("The first letter of the greeting is:" + firstLetter); //This will print the value for the firstLetter
        System.out.println(greeting); // This will print the value of the greeting

    }
}
