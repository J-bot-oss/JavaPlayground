public class variables_part3 {
    public static void main(String[] args) {
        //In this part we are going to explore boolean variables
        // Boolean variables are either true or false
        boolean isAvailable = true;
        boolean isSoldOut = false;

        //To print the boolean values, we use System.out.println()
        System.out.println("Is the item available:" + isAvailable); // This will print the value of isAvailable
        System.out.println("Is the item sold out:" + isSoldOut); // This will print the value of isSoldOut

}


    
}
