public class areaOfARectangle {
    public static void main(String[] args) {
        double length = 5;
        double width = 9;
        double area = length * width;
        System.out.println("So, I have a rectangle of length " + length + "cm and width " + width + "cm");
        System.out.println("It's area is " + area + "sqcm");
    }
}
